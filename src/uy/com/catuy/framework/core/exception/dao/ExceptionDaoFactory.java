package uy.com.catuy.framework.core.exception.dao;

/**
 * Factory para DAO de excepciones.
 *
 * @author Juan Andres Martinez Chaine ($LastChangedBy: 801678 $)
 *
 * @version $LastChangedRevision: 22 $ $LastChangedDate: 2009-09-28 01:12:35 -0300 (Mon, 28 Sep 2009) $
 */
public class ExceptionDaoFactory {

	private static ExceptionDao businessExceptionDao;
	
	private static ExceptionDao infrastructureExceptionDao;

	/**
	 * Obtiene el valor del atributo businessExceptionDao.
	 *
	 * @return el valor de businessExceptionDao
	 */
	public static ExceptionDao getBusinessExceptionDao() {
		return businessExceptionDao;
	}

	/**
	 * Establece el valor del atributo businessExceptionDao.
	 *
	 * @param businessExceptionDao el valor a establecer en businessExceptionDao
	 */
	public void setBusinessExceptionDao(ExceptionDao businessExceptionDao) {
		ExceptionDaoFactory.businessExceptionDao = businessExceptionDao;
	}

	/**
	 * Obtiene el valor del atributo infrastructureExceptionDao.
	 *
	 * @return el valor de infrastructureExceptionDao
	 */
	public static ExceptionDao getInfrastructureExceptionDao() {
		return infrastructureExceptionDao;
	}

	/**
	 * Establece el valor del atributo infrastructureExceptionDao.
	 *
	 * @param infrastructureExceptionDao el valor a establecer en infrastructureExceptionDao
	 */
	public void setInfrastructureExceptionDao(
			ExceptionDao infrastructureExceptionDao) {
		ExceptionDaoFactory.infrastructureExceptionDao = infrastructureExceptionDao;
	}
	
}
